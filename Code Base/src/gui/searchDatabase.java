    private void btnAddToStockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToStockActionPerformed
        // TODO add your handling code here:
        if(txtID.getText().trim().equals("") && txtNameOfProduct.getText().trim().equals("") && txtDateAddedToStock.getText().trim().equals("") )
        {
            JOptionPane.showMessageDialog(null, "Cannot leave fields Empty" , "Error" , JOptionPane.WARNING_MESSAGE);
        
            if(!(Pattern.matches("^[a-zA-z]+$", txtNameOfProduct.getText())))
            {
                JOptionPane.showMessageDialog(null, "The name is invalid", "Error",JOptionPane.WARNING_MESSAGE);
            }
        }
        else
        {
        String ID = txtID.getText();
        String NameOfProduct = txtNameOfProduct.getText();
        String DateAddedToStock = txtDateAddedToStock.getText();
        
        Connection con = null;
        Statement stmt = null;
        
        try
        {
        String url = "jdbc:postgresql://localhost:5432/StylingHouse";
        String username = "postgres";
        String password = "1234";
        con = DriverManager.getConnection(url, username, password);
        PreparedStatement ps = con.prepareStatement("insert into search values(\'"+ID+"\',\'"+NameOfProduct+
            "\', \'"+DateAddedToStock+"\')");
        int status = ps.executeUpdate();
        if (status > 0) 
        {
            JOptionPane.showMessageDialog(null, "Inserted into Database", "Success", JOptionPane.INFORMATION_MESSAGE);
        } 
        else 
        {
            JOptionPane.showMessageDialog(null, "Not able Insert into Database", "Error", JOptionPane.WARNING_MESSAGE);
        }
        con.close();
        ps.close();
        } 
        catch (Exception e) 
        {
        JOptionPane.showMessageDialog(this, e.getMessage());
        }
        }
    }//GEN-LAST:event_btnAddToStockActionPerformed

