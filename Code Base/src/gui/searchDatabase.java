    public ArrayList<Stock> getStockList()
    {
        ArrayList<Stock> stockList =  new ArrayList<Stock>();
        Connection  connection =  getConnection();
        String query = "SELECT * FROM search";
        Statement st;
        ResultSet rs;
        try
        {
            st = connection.createStatement();
            rs = st.executeQuery(query);
            Stock customer;
            while(rs.next())
            {
                customer = new Stock(rs.getInt("id"),rs.getString("ProductName"),
                        rs.getString("DateOfAdditionToStock"));
                stockList.add(customer);
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        return stockList;
    }
   
    public void Show_Stock_In_JTable()
    {
        ArrayList<Stock> list = getStockList();
        DefaultTableModel model = (DefaultTableModel)jTable_Show_Stock.getModel();
        Object[] row = new Object[3];
        for(int i=0; i<list.size();i++)
        {
            row[0] = list.get(i).getID();
            row[1] = list.get(i).getNameOfProduct();
            row[2] = list.get(i).getDateAddedToStock();
            
            model.addRow(row);
        }
    }
    
    public void executeSQLQuery(String query, String message)
    {
        Connection con = getConnection();
        Statement st;
        try
        {
            st = con.createStatement();
            if((st.executeUpdate(query)) == 1)
            {
                JOptionPane.showMessageDialog(null,"Data "+message+"Succefully");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Data Not "+message);
            }
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
        }
    }

