    public ArrayList<emp> getEmpList()
    {
        ArrayList<emp> empList =  new ArrayList<emp>();
        Connection  connection =  getConnection();
        String query = "SELECT * FROM employees";
        Statement st;
        ResultSet rs;
        try
        {
            st = connection.createStatement();
            rs = st.executeQuery(query);
            emp employee;
            while(rs.next())
            {
                employee = new emp(rs.getString("FirstName"),rs.getString("LastName"),rs.getString("Position"),rs.getString("Address"),rs.getString("Description"),rs.getString("DateAdded"),rs.getString("Salary"),rs.getString("Leaves"),rs.getString("Email"),rs.getString("PhoneNumber"));
                empList.add(employee);
            }
        }
        catch(Exception e)
        {
        }
        return empList;
    }
    
    public void Show_Emp_In_JTable()
    {
        ArrayList<emp> list = getEmpList();
        DefaultTableModel model = (DefaultTableModel)jTable_Show_Employees.getModel();
        Object[] row = new Object[10];
        for(int i=0; i<list.size();i++)
        {
            row[0] = list.get(i).getFirstName();
            row[1] = list.get(i).getLastName();
            row[2] = list.get(i).getPosition();
            row[3] = list.get(i).getAddress();
            row[4] = list.get(i).getDescription();
            row[5] = list.get(i).getDateAdded();
            row[6] = list.get(i).getSalary();
            row[7] = list.get(i).getLeaves();
            row[8] = list.get(i).getEmail();
            row[9] = list.get(i).getPhoneNumber();
            model.addRow(row);
        }
    }
    
    public void executeSQLQuery(String query, String message)
    {
        Connection con = getConnection();
        Statement st;
        try
        {
            st = con.createStatement();
            if((st.executeUpdate(query)) == 1)
            {
                JOptionPane.showMessageDialog(null,"Data "+message+"Succefully");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Data Not "+message);
            }
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
        }
    }
