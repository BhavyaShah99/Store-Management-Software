    private void jTable_Show_EmployeesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_Show_EmployeesMouseClicked
        // TODO add your handling code here:
        int i = jTable_Show_Employees.getSelectedRow();
        TableModel model = jTable_Show_Employees.getModel();
        txtFN.setText(model.getValueAt(i,0).toString());
        txtLN.setText(model.getValueAt(i,1).toString());
        txtPosition.setText(model.getValueAt(i,2).toString());
        txtAdd.setText(model.getValueAt(i,3).toString());
        txtDescription.setText(model.getValueAt(i,4).toString());
        txtDateAdded.setText(model.getValueAt(i,5).toString());
        txtSalary.setText(model.getValueAt(i,6).toString());
        txtLeaves.setText(model.getValueAt(i,7).toString());
        txtEmail.setText(model.getValueAt(i,8).toString());
        txtPhoneNumber.setText(model.getValueAt(i,9).toString());
    }//GEN-LAST:event_jTable_Show_EmployeesMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String fn = txtFN.getText();
        String ln = txtLN.getText();
        String po = txtPosition.getText();
        String add = txtAdd.getText();
        String des = txtDescription.getText();
        String da = txtDateAdded.getText();
        String sal = txtSalary.getText();
        String lea = txtLeaves.getText();
        String em = txtEmail.getText();
        String pn = txtPhoneNumber.getText();
       String query = "UPDATE public.employees SET FirstName="+fn+",LastName="+ln+", Position="+po+", Address="+add+", Description="+des+", DateAdded="+da+", Salary="+sal+", Leaves="+lea+", Email="+em+", PhoneNumber="+pn+" WHERE FirstName="+fn;
       executeSQLQuery(query,"Updated");
    }//GEN-LAST:event_btnUpdateActionPerformed

